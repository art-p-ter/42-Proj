#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_numbers(void)
{
	int	ch;

	ch = 48;
	while (ch != 58)
	{
		ft_putchar(ch);
		ch++;
	}
}
