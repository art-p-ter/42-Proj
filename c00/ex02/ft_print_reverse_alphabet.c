#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_reverse_alphabet(void)
{
	int	ch;

	ch = 122;
	while (ch != 96)
	{
		ft_putchar(ch);
		ch--;
	}
}
